    private void txtEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtEnterActionPerformed
        String uText = txtEnter.getText();
        txtChat.append("\n Siz : " + uText + "\n");
        txtEnter.setText("");
        if (uText.contains("selam")||uText.contains("merhaba")||uText.contains("merhabalar")) 
            decideRandom(greetingMessageList);
        else if(uText.contains("naber")||uText.contains("nasilsiniz"))
            decideRandom(halhatirMessageList); 
        else if(uText.contains("urun secmek istiyorum")||uText.contains("urun"))
              urunler();
         else if(uText.contains("1"))
              try {
                  telefon_urunler();
        } catch (IOException ex) {
            Logger.getLogger(chat_ekrani.class.getName()).log(Level.SEVERE, null, ex);
        }
        else if(uText.contains("2"))
              try {
                  pc_urunler();
        } catch (IOException ex) {
            Logger.getLogger(chat_ekrani.class.getName()).log(Level.SEVERE, null, ex);
        }
        else
            decideRandom(exceptionMessageList);

    }//GEN-LAST:event_txtEnterActionPerformed
   
    private void decideRandom(ArrayList<String> messageList) {
        int decider = (int) (Math.random() * messageList.size());
        txtChat.append("\t\t" + messageList.get(decider));
    }
     private void urunler()
     {
         txtChat.append("\t\t" + urunler.get(0));
     }
    private void telefon_urunler() throws IOException
    {   double kiyas=0.0; int yeri=0; double x;
        for(int i =0 ; i<this.t.size();i++)
        { x=yeni.get_TweetPolarity(t.get(i).getModel());
         if(kiyas<x)
        {yeri=i; kiyas=x;}
         txtChat.append("\n\n"+(i+1) +"\n"+ t.get(i).ToString()+ "\n Polarity degerleri : "+x+ "\n En cok like alan tweet : "+yeni.getBest_tweet(t.get(i).getModel()));   }
            
      txtChat.append("\n\n\t Polarity En Yuksek Model\n\n"+(yeri+1) +"\n"+ t.get(yeri).ToString()+ "\n Polarity degerleri : "+kiyas+ "\n En cok like alan tweet : "+yeni.getBest_tweet(t.get(yeri).getModel()));   
    }
